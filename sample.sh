# பயனுள்ள மதிப்பீடுகள்
வெறும் உரைக்கு அப்பால், ஊடாடும் பயிற்சிகள் மூலம் கற்றல்.

---

## வினா வகைகள்
1. **MCQ:** தரமான பலவுள் தெரிவு.
2. **பொருத்துக:** தர்க்க அடிப்படையிலான இணைத்தல்.
3. **சிறந்த விடையைத் தேர்ந்தெடு:** நுணுக்கமான முடிவெடுத்தல்.

---

## பின்னூட்ட சுழற்சிகள் (Feedback Loops)
வெற்றி/எச்சரிக்கை நிலைகளைப் பயன்படுத்தி உடனடி காட்சி பின்னூட்டம் அளித்தல்.

---

## தரவு கண்காணிப்பு
பயிற்சி தொகுதிகள் மூலம் மாணவர்களின் முன்னேற்றம் எவ்வாறு அளவிடப்படுகிறது.
